<?php 

echo "Conditional Operator ( > , < , >= , <= , == , != ) <br>";

/*==========================
 Conditional Operator Start
===========================*/

$number1 = 10;
$number2 = 20;

echo "Less then (<) =" .$number1 < $number2, "<br>";

echo "Greter then (>) =" .$number1 > $number2, "<br>";

echo "Less or equal (<=) =" .$number1 <= $number2, "<br>";

echo "Greter or equal (>=) =" .$number1 >= $number2, "<br>";

echo "Equal (==) =" .$number1 == $number2, "<br>";

echo "Not Equal (!=) =" .$number1 != $number2;

/*========================
 Conditional Operator End
=========================*/

?>